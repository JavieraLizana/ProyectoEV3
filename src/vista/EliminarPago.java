    private void jbtn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_eliminarActionPerformed

        int idPago;

        idPago = Integer.parseInt(this.jtxt_idPago.getText());

        RegistroPago regPago = new RegistroPago();
        int op = JOptionPane.showConfirmDialog(this, "Â¿Seguro desea eliminar? ", "Aviso", 2);

        if (op == 0) {
            regPago.eliminar(idPago);
            JOptionPane.showMessageDialog(null, "Pago eliminado", "Aviso", 1);

        } else {
            JOptionPane.showMessageDialog(null, "Pago NO fue eliminado", "Aviso", 0);

        }


    }//GEN-LAST:event_jbtn_eliminarActionPerformed

